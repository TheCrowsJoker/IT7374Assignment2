package gui;

import java.util.Date;

public class Goals {
	private int wordGoal;
	private Date finishGoal;

	public int getWordGoal() {
		return wordGoal;
	}

	public void setWordGoal(int wordGoal) {
		this.wordGoal = wordGoal;
	}

	public Date getFinishGoal() {
		return finishGoal;
	}

	public void setFinishGoal(Date finishGoal) {
		this.finishGoal = finishGoal;
	}
}
